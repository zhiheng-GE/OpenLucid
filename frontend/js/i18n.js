// ── Language detection ──────────────────────────────────────────────────────

const _stored = localStorage.getItem('od_lang');
const _detected = (navigator.language || '').startsWith('zh') ? 'zh' : 'en';
window._odLang = _stored || _detected;


// ── Page namespace auto-detection ───────────────────────────────────────────

(function() {
  const path = location.pathname.replace(/^\//, '').replace(/\.html$/, '') || 'index';
  // Map URL path to I18N namespace
  const nsMap = {
    'index': 'index',
    'install': 'install',
    'signin': 'signin',
    'offer': 'offer',
    'apps': 'apps',
    'setting': 'setting',
    'account': 'account',
    'topic-studio': 'topic_studio',
    'kb-qa': 'kb_qa',
    'script-writer': 'script_writer',
    'strategy-unit': 'strategy_unit',
    'brandkit': 'brandkit',
    'brandkit-list': 'brandkit_list',
  };
  window._odPageNs = nsMap[path] || null;
})();


// ── Global t() function ─────────────────────────────────────────────────────

window.t = function(key) {
  const lang = window._odLang;
  // 1. Try page namespace
  if (window._odPageNs) {
    const pageDict = window.I18N[window._odPageNs];
    if (pageDict && pageDict[lang] && pageDict[lang][key] !== undefined) {
      return pageDict[lang][key];
    }
  }
  // 2. Try common namespace
  const commonDict = window.I18N.common;
  if (commonDict && commonDict[lang] && commonDict[lang][key] !== undefined) {
    return commonDict[lang][key];
  }
  // 3. Fallback: return key itself
  return key;
};


// ── API language helper ─────────────────────────────────────────────────────

window.getApiLang = function() {
  return window._odLang === 'zh' ? 'zh-CN' : 'en';
};


// ── Language switcher ───────────────────────────────────────────────────────

window.setOdLang = function(val) {
  window._odLang = val;
  localStorage.setItem('od_lang', val);
};


// ── Alpine magic $t — ensures t() is accessible in all Alpine expressions ──

document.addEventListener('alpine:init', () => {
  if (window.Alpine) {
    Alpine.magic('t', () => window.t);
    Alpine.magic('getApiLang', () => window.getApiLang);
  }
});
